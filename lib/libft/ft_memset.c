#include "libft.h"

void	*ft_memset( void *str, int value, size_t count)
{
	size_t	i;
	char	*mod;

	mod = str;
	i = 0;
	while (i < count)
	{
		mod[i] = value;
		i++;
	}
	return (str);
}
